package localidades;

public class PuntoPrincipal {
    int indice;
    double latitud;
    double longitud;
    String localidad;

    public PuntoPrincipal(int indice, double latitud, double longitud, String localidad) {
        this.indice = indice;
        this.latitud = latitud;
        this.longitud = longitud;
        this.localidad = localidad;
    }
}